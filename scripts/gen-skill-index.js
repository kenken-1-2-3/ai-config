#!/usr/bin/env node
// Generate a "Skills Index" markdown section for a project by scanning its
// SKILL.md files, so every agent session sees the full inventory of skills the
// repo has (ai-config-shared + repo-native) and when to use each.
//
// Usage: node gen-skill-index.js <project_path>
// Prints the section to stdout; prints nothing (exit 0) if the repo has no skills.
//
// Scanned locations, in priority order:
//   .claude/skills/<name>/SKILL.md  -> Claude Code auto-registers these ([auto])
//   skills/<name>/SKILL.md          -> repo-native, not auto-registered ([read])
// A name found in the higher-priority location wins (no duplicate rows).

const fs = require("fs");
const path = require("path");

const projectPath = process.argv[2];
if (!projectPath) process.exit(0);

const LOCATIONS = [
  { dir: ".claude/skills", auto: true },
  { dir: "skills", auto: false },
];

function parseFrontmatter(text) {
  const m = text.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  const fm = {};
  if (!m) return fm;
  for (const line of m[1].split(/\r?\n/)) {
    const mm = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (mm) {
      let v = mm[2].trim();
      if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
        v = v.slice(1, -1);
      }
      fm[mm[1]] = v;
    }
  }
  return fm;
}

const seen = new Set();
const skills = [];
for (const loc of LOCATIONS) {
  const base = path.join(projectPath, loc.dir);
  let entries;
  try {
    entries = fs.readdirSync(base, { withFileTypes: true });
  } catch {
    continue;
  }
  for (const e of entries) {
    if (!e.isDirectory()) continue;
    const skillMd = path.join(base, e.name, "SKILL.md");
    if (!fs.existsSync(skillMd)) continue;
    const fm = parseFrontmatter(fs.readFileSync(skillMd, "utf8"));
    const name = fm.name || e.name;
    if (seen.has(name)) continue;
    seen.add(name);
    skills.push({
      name,
      description: fm.description || "(no description)",
      rel: path.join(loc.dir, e.name, "SKILL.md"),
      auto: loc.auto,
    });
  }
}

if (skills.length === 0) process.exit(0);

skills.sort((a, b) => a.name.localeCompare(b.name));

const lines = [];
lines.push("<!-- BEGIN skills-index (generated by install.sh — do not edit) -->");
lines.push("# Skills Index (this project)");
lines.push("");
lines.push(
  "Skills this repo provides and when to use each. When a trigger matches, read the referenced `SKILL.md` and follow it. `[auto]` skills live under `.claude/skills/` and Claude Code also loads them automatically (or invoke `/<name>`); `[read]` skills are repo-native and are not auto-registered, so read their `SKILL.md` yourself when the trigger matches."
);
lines.push("");
for (const s of skills) {
  lines.push(`- **${s.name}** [${s.auto ? "auto" : "read"}] — ${s.description} — \`${s.rel}\``);
}
lines.push("");
lines.push("<!-- END skills-index (generated by install.sh — do not edit) -->");

process.stdout.write(lines.join("\n") + "\n");
